										<h5>ARTICULO 10</h5>
										<p class="text-justify">Se prohíbe la prestación de servicios de trasplante de órganos y tejidos a extranjeros no residentes en el territorio nacional, salvo que el receptor sea cónyuge o compañero permanente, pariente en cuarto grado de consanguinidad, segundo de afinidad o primero civil, del donante. El Ministerio de Salud podrá autorizar de manera transitoria los trasplantes a extranjeros no residentes cuando se compruebe debidamente que los tejidos disponibles son suficientes para cubrir la demanda interna. En todo caso los nacionales y los extranjeros residentes tendrán prelación. </p>
